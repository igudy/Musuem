              <h3 class="text-black mb-3">Nok terracotta figures and a terracotta Ife head</h3>BOSTON, MA (June 26, 2014)—The Museum of Fine Arts, Boston (MFA), has reached an agreement with the National Commission of Museums and Monuments, Nigeria (NCMM), transferring to the Commission eight antiquities of Nigerian origin that are believed to have been the subject of illicit trafficking.
The antiquities include two Nok terracotta figures and a terracotta Ife head, archaeological materials that are known to be at high risk for theft and looting. The group also includes an ekpu, or ancestral figure dating to the 18th or 19th century, which was part of the collection of the Oron Museum, near Calabar, Nigeria, as late as the 1970s; and a bronze altar figure of about 1914, which was likely stolen from the Royal Palace in Benin City in 1976. Two terracotta heads produced in the Kingdom of Benin and a group of Kalabari screen figures appear to have been illegally exported.
The MFA received the objects in the bequest of a local collector of African art, who acquired all eight objects in good faith in the 1990s from dealers in the United States and Europe.
The Museum began the process of researching the provenance (or history of ownership) of the objects after receiving notification of the bequest. Recognizing that these eight objects were probably illegally removed from Nigeria in recent years, and that their export would have been regulated by Nigeria’s National Commission for Museums and Monuments Act (chapter 242) of 1990, the MFA contacted the NCMM to seek its authorization before proceeding with their acquisition. The NCMM swiftly responded that the export of these objects had not been approved; and, indeed, that several documents which purportedly authorized their sale and export were forged. Upon receipt of this information, the MFA began to arrange for the return of the objects to Nigeria, which were received by Nigerian authorities earlier this month.
The objects transferred to Nigeria from the MFA are:
1.  Head
African, Edo peoples, Nigeria, Benin kingdom, about 1750
2.  Memorial screen (duen fubara)
African, Ijaw Kalabari peoples, Nigeria, late 19th century
3.  Head
African, Nok peoples, Nigeria, About 500 B.C.–A.D. 200
4.  Head of an Oba
Edo peoples, Benin Kingdom, Nigeria, 19th century
5.  Male Figure
African, Nok peoples. Nigeria, About 500 B.C.–A.D. 200
6.  Portrait head
African, Yoruba peoples, Ife Kingdom, Nigeria, 12th–14th century
7.  Oron Ancestral Figure (Ekpu)
Oron peoples, southeastern Nigeria
8.  Altar figure
Benin peoples, Nigeria
The Teel Collection at the Museum of Fine Arts, Boston
The MFA received the eight Nigerian objects as part of the bequest from the late William E. Teel. The Teel bequest includes more than 300 African and Oceanic works, along with several Ancient American and Native American pieces and a small group of European and American works on paper. Teel and his wife Bertha, who passed away in 1995, were enthusiastic collectors who fostered appreciation of the art of sub-Saharan Africa and Oceania in Boston and beyond. The Teels built an outstanding collection, and played a significant role in placing such works in the domain of fine art in the city. As a result of their long-term support, including the endowment of a curatorial post for African and Oceanic art, the MFA has been able to significantly build its collection of African art. A selection of works from the bequest, mostly from west and central Africa, is now on view in the MFA’s recently refreshed Arts of Africa Gallery. Information regarding the eight antiquities transferred to Nigera is available in the Provenance section.
Provenance Research at the Museum of Fine Arts, Boston
The MFA is a leader in the field of provenance research, employing a full-time Curator for Provenance, who works with curators throughout the Museum to research and document the MFA’s collection on an ongoing basis. Findings are included in the Museum’s online collections database. The MFA follows the highest standards of professional practice in regards to issues of ownership and in its response to claims for works in the collection. If research demonstrates that a work of art has been stolen, confiscated or unlawfully appropriated without subsequent restitution, then the Museum will notify potential claimants, and seek to resolve the matter in an equitable, appropriate and mutually agreeable manner. A list of ownership resolutions at the Museum since the late 1990s can be found here.
The Museum of Fine Arts, Boston (MFA), is recognized for the quality and scope of its encyclopedic collection, which includes an estimated 500,000 objects. The Museum’s collection is made up of: Art of the Americas; Art of Europe; Contemporary Art; Art of Asia, Oceania, and Africa; Art of the Ancient World; Prints, Drawings, and Photographs; Textile and Fashion Arts; and Musical Instruments. Open seven days a week, the MFA’s hours are Saturday through Tuesday, 10 a.m. – 4:45 p.m.; and Wednesday through Friday, 10 a.m. – 9:45 p.m. Admission (which includes one repeat visit within 10 days) is $25 for adults and $23 for seniors and students age 18 and older, and includes entry to all galleries and special exhibitions. Admission is free for University Members and youths age 17 and younger on weekdays after 3 p.m., weekends, and Boston Public Schools holidays; otherwise $10. Wednesday nights after 4 p.m. admission is by voluntary contribution (suggested donation $25). MFA Members are always admitted for free. The MFA’s multi-media guide is available at ticket desks and the Sharf Visitor Center for $5, members; $6, non-members; and $4, youths. The Museum is closed on New Year’s Day, Patriots’ Day, Independence Day, Thanksgiving, and Christmas. For more information, call 617.267.9300. The MFA is located on the Avenue of the Arts at 465 Huntington Avenue, Boston, MA 02115.
